// Nested if-else 
const money = 50;
if(money > 300){
    console.log("Lets go shopping");
}
else{
    if(money > 100){
        console.log("Lets go shopping for some food");
    }
    else{
        if(money > 0){
            console.log("Lets go shopping for some clothes");
    }
    else{
        console.log("Lets go shopping for some food and clothes");
    }
}
}